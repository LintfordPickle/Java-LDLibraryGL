package net.lintford.library.data.entities;

public interface ILineEntity {

	/** @return the x component of the start of the line entity */
	public abstract float x1();

	/** @return the y component of the start of the line entity */
	public abstract float y1();

	/** @return the x component of the end of the line entity */
	public abstract float x2();

	/** @return the y component of the end of the line entity */
	public abstract float y2();

}
